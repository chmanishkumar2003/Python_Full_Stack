import math
# This is a simple math operation
n=125.548
print("The square root of", n, "is", math.sqrt(n))
print("The square of", n, "is", n**2)
print("Ceiling of", n, "is", math.ceil(n))
print("Floor of", n, "is", math.floor(n))
print("Logarithm of", n, "is", math.log(n))
print("Exponential of", n, "is", math.exp(n))